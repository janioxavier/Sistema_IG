    private void initText() {
        representacao = "";
        String[] jogadores = dadoPlotagem.getJogadores();
        for (String jogador : jogadores) {
            representacao += jogador + "\n";
        }
        if (representacao.equals("")) {
            representacao = "Nenum jogador cadastrado";
        }
        jLabeljogadores.setText(representacao);
    }

