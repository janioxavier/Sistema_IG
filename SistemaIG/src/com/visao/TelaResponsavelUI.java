    private void personagensJListKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_personagensJListKeyPressed
        // TODO add your handling code here:
        String selected = personagensJList.getSelectedValue();
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_KP_UP:
            case KeyEvent.VK_KP_DOWN:
            case KeyEvent.VK_UP:
            case KeyEvent.VK_DOWN:
                atualizarPainelProgresso(selected);
            default:
                break;
        }        
    }//GEN-LAST:event_personagensJListKeyPressed

