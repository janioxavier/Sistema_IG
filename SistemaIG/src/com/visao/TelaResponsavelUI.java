    private void personagensJListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_personagensJListMouseClicked
        // TODO add your handling code here:
        String selected = personagensJList.getSelectedValue();
        atualizarPainelProgresso(selected);
    }//GEN-LAST:event_personagensJListMouseClicked

    private void atualizarPainelProgresso(String nickJogador) {
        String texto;
        personagemJLabel.setText(
                dadoPlotagem.getPersonagemMaisEscolhido(nickJogador)[0]);
        AcaoJLabel.setText(dadoPlotagem.getAcaoMaisEscolhida(nickJogador)[0]);

        for (TipoPersonagem tipo : TipoPersonagem.values()) {
            texto = Integer.toString(dadoPlotagem.getMaiorNivelPersonagem(nickJogador, tipo));
            switch (tipo) {
                case GATO:
                    nivelGatoJLabel.setText(texto);
                    break;
                case CACHORRO:
                    nivelCachorroJLabel.setText(texto);
                    break;
                case MENINA:
                    nivelMeninaJLabel.setText(texto);
                    break;
                case MENINO:
                    nivelMeninoJLabel.setText(texto);
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Tipo: "
                            + "" + tipo.getNome() + " inválido");
            }
        }

    }

